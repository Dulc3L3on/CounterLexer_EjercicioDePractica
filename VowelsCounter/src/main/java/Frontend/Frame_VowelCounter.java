    private void btn_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CalcularActionPerformed
        Reader reader = new StringReader(txtArea_input.getText()+"\n");//he agregado este salto de lÃ­nea para que asÃ­ al Ãºltimo bloque en caso de ser alfa#, se le cuente el nÃºmero de vocales xD, esto es para evitar add un %endof{ %endof} al lexer, para repetir el cÃ³digo de contar vocales en caso de que numeroVocales > 0
        lexer = new CounterLexer(reader);
        
        try {
            while(lexer.yylex() != CounterLexer.YYEOF){//quizÃ¡ tb ERROR... bueno auqneu si da esto deberÃ­a tratarse correctamente          
                //puede que de un error por hacer girar el lexer y no invocarlo solo dos veces como lo hizo el inge [aunque mÃ¡s pienso que se equivocÃ³ xD]
                //o por el warning de que incorrecto hace match con "empty string" 
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR);
        }
        
        manejadorRespuesta = new ManejadorRespuesta(lexer.getListaResultadosVocales(), 
                lexer.getListaResultadosEnteros(), lexer.getListaErrores());
        
        manejadorRespuesta.setResultados(table_Resumen, lexer.getCantidadResultadosTotalesPorTipo(),
                table_detalleResultados_Vocales, table_detalleResultados_Enteros, table_detalleErrores);
    }//GEN-LAST:event_btn_CalcularActionPerformed

